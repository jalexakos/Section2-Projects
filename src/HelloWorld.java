public class HelloWorld {
  // Think of a class as an entity that can hold data and behaviors (operations).
  // In this class, we have a single method (e.g. behavior).

  public static void main(String[] args) {
    // System is a class
    // out is an object in that class that gives us access to the output stream
    // println is a method of the object out
    System.out.println("Hello world!");
    System.out.println("Hello Josh!");
  }
}
